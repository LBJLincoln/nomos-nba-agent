def run_openclaw_skill(skill_name, args=None):
    """
    Execute an OpenClaw skill via subprocess.
    Falls back to direct API calls if openclaw CLI is not available.

    Args:
        skill_name: Name of the skill (e.g., 'sports-odds', 'nba-stats')
        args: Dict of arguments to pass to the skill

    Returns:
        dict with skill output or error
    """
    log(f"[OpenClaw] Running skill: {skill_name} with args: {args}")
    result = {"skill": skill_name, "timestamp": datetime.now(timezone.utc).isoformat(), "success": False}

    # Try openclaw CLI first
    openclaw_bin = shutil.which("openclaw")
    if openclaw_bin:
        try:
            cmd = [openclaw_bin, "skill", "run", skill_name]
            if args:
                cmd.extend(["--args", json.dumps(args)])
            proc = subprocess.run(
                cmd,
                capture_output=True,
                text=True,
                timeout=60,
                env={**os.environ, "OPENCLAW_CONFIG": str(OPENCLAW_CONFIG)},
            )
            if proc.returncode == 0:
                result["output"] = proc.stdout.strip()
                try:
                    result["data"] = json.loads(proc.stdout)
                except (json.JSONDecodeError, TypeError):
                    result["data"] = proc.stdout.strip()
                result["success"] = True
                log(f"[OpenClaw] Skill {skill_name} completed via CLI")
            else:
                result["error"] = proc.stderr.strip() or f"Exit code {proc.returncode}"
                log(f"[OpenClaw] Skill {skill_name} CLI failed: {result['error'][:200]}")
        except subprocess.TimeoutExpired:
            result["error"] = "Timeout after 60s"
            log(f"[OpenClaw] Skill {skill_name} timed out")
        except FileNotFoundError:
            result["error"] = "openclaw binary not found"
            log(f"[OpenClaw] openclaw binary not found, falling back to direct API")
    else:
        log(f"[OpenClaw] CLI not available, using direct API fallback for {skill_name}")

    # Fallback: direct API implementation for known skills
    if not result["success"]:
        if skill_name == "sports-odds":
            result = _skill_sports_odds_fallback(args or {})
        elif skill_name == "nba-stats":
            result = _skill_nba_stats_fallback(args or {})
        else:
            result["error"] = f"Unknown skill and no CLI: {skill_name}"

    # Save result
    out_file = RESULTS_DIR / f"openclaw-skill-{skill_name}.json"
    out_file.write_text(json.dumps(result, indent=2, default=str))
    return result


