def _skill_nba_stats_fallback(args):
    """Fallback: fetch NBA stats via nba_api if available."""
    log("[OpenClaw] nba-stats fallback: attempting nba_api")
    result = {
        "skill": "nba-stats",
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "success": False,
        "source": "nba_api-fallback",
    }
    try:
        from nba_api.stats.endpoints import ScoreboardV2
        from datetime import date
        scoreboard = ScoreboardV2(game_date=date.today().strftime("%m/%d/%Y"))
        games = scoreboard.get_normalized_dict()
        result["data"] = {
            "date": date.today().isoformat(),
            "games": games.get("GameHeader", [])[:20],
            "line_scores": games.get("LineScore", [])[:40],
        }
        result["success"] = True
        log(f"[OpenClaw] nba-stats: fetched {len(result['data']['games'])} games")
    except Exception as e:
        result["error"] = str(e)
        log(f"[OpenClaw] nba-stats fallback failed: {e}")
        # Final fallback: ask LiteLLM
        response = call_litellm(
            "You are an NBA stats expert. Provide today's game schedule and key stats.",
            "What NBA games are scheduled today? List teams, projected starters, and key injuries.",
            model="fast", max_tokens=1500,
        )
        if response:
            result["data"] = {"llm_analysis": response[:2000]}
            result["success"] = True
            result["source"] = "litellm-fallback"
    return result


def _run_openclaw_agent(agent_name, system_prompt, user_prompt, model_id=None):
    """
    Run a single OpenClaw agent task via OpenRouter API (or LiteLLM fallback).
    Returns the agent's response text.
    """
    if not model_id:
        model_id = OPENROUTER_MODEL_HEALER

    # Try OpenRouter directly if we have a key
    if OPENROUTER_API_KEY:
        import urllib.request
        body = json.dumps({
            "model": model_id.replace("openrouter/", ""),
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt},
            ],
            "max_tokens": 3000,
            "temperature": 0.3,
        }).encode()
        headers = {
            "Content-Type": "application/json",
            "Authorization": f"Bearer {OPENROUTER_API_KEY}",
            "HTTP-Referer": "https://nomos-nba-quant.hf.space",
            "X-Title": "NOMOS NBA Quant",
        }
        req = urllib.request.Request(f"{OPENROUTER_BASE_URL}/chat/completions", data=body, headers=headers)
        try:
            resp = urllib.request.urlopen(req, timeout=120)
            data = json.loads(resp.read().decode())
            choices = data.get("choices", [])
            if choices:
                return choices[0].get("message", {}).get("content", "")
        except Exception as e:
            log(f"[OpenClaw] OpenRouter call failed for {agent_name}: {e}")

    # Fallback to LiteLLM
    return call_litellm(system_prompt, user_prompt, model="smart", max_tokens=3000)


