# Model routing: logical names → actual model identifiers
# "healer" and "hunter" are FREE OpenRouter stealth models
MODEL_ROUTES = {
    "smart": "smart",                           # LiteLLM model group (13-provider fallback)
    "fast": "fast",                             # LiteLLM model group (quick tasks)
    "default": "default",                       # LiteLLM model group (fallback)
    "healer": "openrouter/healer-alpha",        # FREE, omni-modal, 262K context
    "hunter": "openrouter/hunter-alpha",        # FREE, 1T params, 100K context, best for agents
}


def call_litellm(system_prompt, user_prompt, model="smart", max_tokens=4000, temperature=0.3):
    """Call LiteLLM proxy (S7) for research tasks.

    Model routing:
      - "smart" / "fast" / "default" → existing LiteLLM model groups
      - "healer" → openrouter/healer-alpha (FREE, omni-modal, 262K context)
      - "hunter" → openrouter/hunter-alpha (FREE, 1T params, 100K context, best for agents)
    """
    import urllib.request

    # Resolve model name through routing table
    resolved_model = MODEL_ROUTES.get(model, model)

    body = json.dumps({
        "model": resolved_model,
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ],
        "max_tokens": max_tokens,
        "temperature": temperature,
    }).encode()

    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Bearer {LITELLM_KEY}",
    }
    req = urllib.request.Request(f"{LITELLM_URL}/v1/chat/completions", data=body, headers=headers)
    try:
        resp = urllib.request.urlopen(req, timeout=180)
        data = json.loads(resp.read().decode())
        choices = data.get("choices", [])
        if choices:
            return choices[0].get("message", {}).get("content", "")
        return ""
    except Exception as e:
        log(f"LiteLLM call failed (model={resolved_model}): {e}")
        return ""


